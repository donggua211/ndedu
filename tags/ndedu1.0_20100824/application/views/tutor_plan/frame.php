          现在还清楚地记得那句让我印象深刻的那些话。经过系统的测评和分别的访谈之后，咨询老师对我说："由于您爱人经常出差，所以您在情感上有些依赖孩子，无法在孩子面前建立起威信，以至于您的说教不但不起作用，反而让孩子越来越皮。建议您，说教不必多，但说了就一定要坚决执行，比如……"
