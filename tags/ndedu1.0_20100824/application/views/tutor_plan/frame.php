希望这些话对其他的家长选择教育机构时有帮助，同时希望尼德教育越办越好。
<span class="font_gray">--人大附小 李同学 妈妈</span><br/>

记得当初我爸带我上辅导班，我就很反对，开始几个辅导学校都没上，后来尼德教育的咨询老师吸引了我，再后来尼德教育的学科老师、素质老师让我成绩越来越好，而且越学越有劲，越来越有兴趣，所以我主动写这封信感谢尼德教育。
我爸自己开了一个小饭店，整天都比较忙，平时很少和我交流，但是他对我学习却格外在意，每次写作业、考试，凡是和学习有关的都要过问。
上初一后，我还是按照以前那样学习，但是我觉得很费力，成绩也开始下降，我爸挺着急，开始指导和监督我的学习……
那天，他突然走进我的房间，把我正在偷看的一本课外书（和同学借的）抢了过来，当场撕碎，他的脾气让我很害怕，我都不知道后来时间怎么过去的。
我不知道尼德教育的咨询老师怎么知道的这件事情，而且当着这位老师面，我爸爸居然给我道歉说"他那天不应该撕我的书，他错了。"我当时简直不敢相信。
也就是这件事情让我决定在尼德教育学习，不知为什么，我就觉得那里老师特别好，他们似乎很明白我心里怎么想的似的，我感到非常开心，而且我的学习成绩也开始越来越好，父母对我的态度也越来越好，他们开始和我谈我的同学、聊我的学校……
马上就要升初三了，我决定继续在尼德教育学习，我希望尼德教育能让我考上重点高中。所以我感谢尼德教育，感谢所有给我上课的教师，希望尼德教育越办越好，也希望其他的学弟学妹们能来这里学习，因为的确和学校以及其他辅导班的感觉不一样。

<span class="font_gray">--一位初二年级女孩</span>
