			<h3>具体体现：</h3>
			<ul>
				<li>3～6岁幼儿最好的教师是父母，你的教育依据不同年龄不同教育需求，设计了具有实际操作性的幼儿教育课程，促进幼儿身体、智力、性格和情感的全面发展，为未来小学学业生活做好准备。</li>
				<li>综合考虑幼儿个性特点、家庭成员背景情况，帮助家庭为幼儿设计不同主题情景学习区（星空、梦幻、数字、彩虹等），辅助幼儿成长。</li>
				<li>针对不同学前幼儿综合发展不同，结合小学学习生活特点，辅助每一位学龄儿童学员顺利度过幼小衔接关键期，快速适应小学学习成长生活。</li>
				<li>帮助学员从小成长良好的学习习惯（注意力、马虎）、生活习惯（自理能力、不受约束）、行为习惯（磨蹭、言行举止），确保学员学习成绩优异、学校生活快乐，升学顺畅。</li>
				<li>从不同儿童不同成长环境、不同个性思维特点、不同成长需求辅助小学低年级（1～3）学员到小学高年级（4～6）学员平稳过渡，该衔接期的平稳过渡相对整个黄金学习期学习至关重要。</li>
				<li>针对不同学员目前学习方法（偏科、厌学、缺乏上进心）、个性发展（任性、缺乏责任感、孤僻）、人际交往（老师冲突、同伴关系）情况，帮助每一位学员总结并掌握适合自己的学习方法、健全个性发展、提高人际交往能力。</li>
				<li>综合考虑学员学习、兴趣爱好、知识结构、家庭环境，帮助学员建立个性化家庭图书屋，精选优质适合学员年龄、心理特点的图书，强化学员兴趣爱好方向，补全知识结构盲区，建立其在某一方面的的发展优势，逐渐延伸到其他方面，最终实现整体发展。</li>
				<li>引导适龄学员了解认识青春期生理、心理特点，安然度过青春期，不早恋、不迷恋网络。</li>
				<li>从初中开始建立学员职业意识，初步了解社会行业，比如财政税收、国家军队、国企私企等，引导思考个人兴趣与个人发展的关系，明确学习的意义和价值。</li>
				<li>高中阶段的学习基本上是一个自我学习的过程（前提是之前学习成长顺利），家长、学校和学员之间更多的强调关系而非管教。</li>
				<li>综合考虑不同年龄学员不同家庭成员文化水平、教育观念，针对学员目前学习发展情况，引导家长认识先进教育理念、给予家长合适教育建议、提供切实可行的教育方法。</li>
				<li>通过你的教育学科辅导和成长增值课程，帮助建立严谨的科学逻辑思维，主动追求、探究知识的兴趣与爱好；塑造独立、自信、坚强等个性品质，迎接大学学习生活。</li>
				<li>顺利考上理想中的大学，具备符合现代社会的综合素质，满足社会需要，实现成功的人生。</li>
