	明确就是指学习目标要便于对照和检查。如："今后要努力学习，争取更大进步"这一目标就不明确，怎样努力呢？哪些方面要有进步？如果必为："数学课语文课都要认真预习。数学成绩要在班级达到中上水平。"这样就明确了，以后是否达到就可以检查了。 
