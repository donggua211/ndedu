	    因此，要想放松孩子的神经，家长必须先放松自己的神经，尽量克制自己的紧张情绪，不让孩子看出来自己心神不宁。一些家长在孩子准备复习期间，简直就变成了一个神经质的人。一位考生说：“我妈这一个月来，变得有点奇奇怪怪的：竟然改了作息时间，时常在家；对所有我接触的朋友问东问西，严然一副侦察敌情的样子；还经常半夜‘失眠’来我房间探察，对我刻苦钻研的精神极度不信任……结果，妈妈明显憔悴了很多，而我突然也觉得我自己考试没有过多压力竟也会是一种过错！”考生们普遍反映，最怕看到老爸焦急的目光，最怕听到老妈不停的唠叨。其实父母是好心，期待孩子在考场上露光彩。问题是焦急的目光会灼伤孩子的自信；没完没了的唠叨会压抑孩子的自尊。过高的期望落在孩子头上，不是动力，而是沉重的压力，使孩子产生委屈、气馁的情绪。孩子缺少阅历，若发现成年人都焦虑不安六神无主了，他还不慌乱？所以，劝家长，您再急，也得内紧外松，稳住阵脚，给孩子减压，鼓励他轻松上阵。要让孩子感受到父母的信任和安稳。信任产生自尊，自尊激发自强；安稳培养镇静，带来复习的高效。家其实木是战场，也不是竞争的手段，而是孩子最不需要防范的安全港湾，这里的一切应当是平和的和安全的。
	       家长应当学会针对孩子的特点来对待他们。有一种孩子，考前复习好像并不紧张，热情和投入不够，此时，家长应当寻找原因。通常这类孩子觉得自己能力有限，不可能在考试中取得好成绩，即使拼命学也得不到好结果，干脆这样混算了，即使考不好，也不丢人。实际上这种消极心态是一种自我保护的逃避心理，是不实事求是的表现。此时，家长应当更多地关心孩子的学习，多询问、多约束、多督促，尤其要关心孩子的学习进度和水准，帮助他们联系家教或补习，让孩子感受到家长的期望和压力。而对于考前紧张和充满压力的孩子，家长则应适当减少关注，而要表现出对孩子很有信心的样子，但这信心不是表现为总是对孩子说“你一定要考上……”“你一定能实现自己的理想……”而是临危不惧的态度和从容，即像平常一样对待孩子，不因孩子要考试而改变自己。
	    家长甚至可以以幽默的口吻来与孩子谈论考试，如“如果考不上学，你就会经常可以和爸爸在一起了，那可是真不错”，“我从来就没有想过你能考上某某大学，那可不是随便想的”，“我看读什么专业都不错，只要能干，行行出状元”。还可以与孩子探讨“考不上怎么办”的问题，不要回避它。回避会让孩子认为家长没有任何考不上的打算，从而加重思想包袱。
	    （2）家长不拿别人来压孩子，尊重孩子的能力和人格。一些家长在考前总爱用榜样人物或别人家的孩子来激励自己孩子学习，这十有八九会产生不良后果。如有的家长总把这样的话挂在嘴边上：“看XX家的XX多用功，一点不像你。要不她能考上北京大学？”“我们同事的孩子可不得了，轻松地考上名牌大学，还是最好的系呢。”当家长有意无意这样说的时候，一般起不到鼓励孩子学习的作用，可能会伤害孩子的自尊。面对中考或高考，孩子们一般在心态上都是非常重视的，在学习动机和目标上并不差，他们通常都是因为能力和学习效果不佳而自责和逃避。家长一般不用在学习动机和理想前途上教育子女，应当重视能力和复习效果，用不着通过比较来激发学习兴趣，这种激发可以在平时进行。
	    （3）信任孩子的自学能力，把学习的主动权交给孩子。高考复习，是一种自主性极强的活动。有经验的教师，往往会随着考试的临近而适当减少对学生学习的具体要求，给他们～定的自主学习时间。学校尚且如此，家庭更需提倡。因为孩子在家中的复习，一般都是在学校紧张学习了一日、一周后的调整性、补充性学习。在这种业余性的学习中，学生的心理应该是放松的，应该是根据自己的身体状况、学习心态、知识掌握优劣的自我感受程度而进行自主安排的。如果他此时备感疲倦，休息便是他的第一需要；如果他延续着学习的欲望，抓紧复习就是自觉的行为；如果清楚自己学习的薄弱环节，那他就会把有限的时间投入到最重要的知识点的复习上。如果家长不考虑这一特点，在孩子复习过程中进行过多的干涉，强迫孩子按自己的意愿安排复习或责备孩子不会学习，会使孩子处于一种不安全的状态，学习的主动性和乐趣性会受到挫伤。       
	如果孩子觉得在自己熟悉的教室温习课业比较自在，就不要强迫他留在家里或上图书馆念书。凡事要以孩子习惯的方式进行，不要到处打听别人的经验，回到家中让孩子效仿，这样只会打乱他的学习规律，反而降低效率。在这方面，家长要树立这样一个观念：我不能替他考试，而要在学习上信任和尊重他。
	    （4）拥有切实的目标和期望值。任何家长都对参加考试的孩子有一个期望值和目标，这个目标通常是根据孩子的实力形成的。比如，根据平时的成绩，你会期望孩子报考重点大学或一般大学。但也有个别家长对孩子的期望超出其能力和努力范围，不切实际。有的家长给孩子定的目标太高，如“非本科一类不读”、‘唯清华北大不上”等等。这种带有“必须”、“一定”字眼的话语，很容易导致孩子紧张和不安。
	    总之，考前的家庭环境对学生高考的成败虽不起决定作用，但却有着直接而不可忽视的影响。家长既要摆正对考试的态度，也要坚持一种实事求是的态度对待孩子的成绩和学习。只有家长保持豁达开通的心境，才能以平常心对待孩子，才能让孩子以平常心走进考场。
	2．保持舒适的家庭环境
	        一些心理素质不好的家长，在孩子考试前不知应当做什么好，往往显得格外殷勤和小心，这会使家中有一种大敌当前的气氛。许多家长在饮食、生活起居方面给予孩子过分的照顾，使孩子有透不过气的感觉。如有的家长不辞辛苦地为孩子找来各种复习资料，并对孩子语重心长地说什么“十年寒窗，在此一举”；还有的家长在孩子复习时，小心翼翼、蹑手蹑脚地送去饮料，让孩子只管学习，什么家务劳动都不要做；有的家长想尽一切办法做很多好吃的让孩子吃，边吃边劝“吃多了身体好，考得才会好”；还有的家长在考前请假来照顾孩子的饮食起居，等等。这一切行动只是在向孩子传达一个信息：现在可是关键时期，全家的头等大事就是你的考试，万万不能有任何闪失。感受到这一点的孩子就会感到压力：家长把一切事业都放了下来，把一切希望都寄托在我身上，如果考不好，对木起家长。这不利于孩子承担考试的责任，反而使其有～种替父母考试的感觉，有一种掌握父母命运的感觉。要一个孩子背起整个家庭命运的包袱，他能不紧张吗？
