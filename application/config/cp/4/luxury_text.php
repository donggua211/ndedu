	当然，这一招不能随便用，有些孩子缺少自觉性，学习不专心，需要大人的督促。如果你有意地打断了他的学习，让他适当做家务，他会很高兴。因为只要不学习，就是他的乐趣。对于这样的学生，家长不要采用这个方法。只是对自觉性过强、焦虑感严重的学生，可以这样做。
