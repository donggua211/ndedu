	文综
	有人说，高考是“六分知识水平、三分心理状态、一分运气”。考生应做好考前的心理准备。在心理学理论中有一个定律：随着考试焦虑水平的增加，个体积极性、主动性以及克服困难的意志力也会随之增强，会对学习、考试效果起到促进作用；因此考生在走进考场之前或进入考场之后，稍微紧张是很正常的现象。
	高考文综的有效时间只有150分钟，争取时间的最大利用价值，是分值体现的基础条件。这就需要把整份试卷当作一个整体来作宏观的考虑，审定重点，把握难点，科学而合理地安排其中的一分一秒。
	先找出自己非常熟悉的送分题马上下手，不管是在试卷哪一部分，做完这些送分题，考试信心就起来了，思维也活跃起来，然后再去解答中档题就容易打开思路，这样就有80%的胜利在握，因为卷面中低中档题占80%的分值。最后再一鼓作气去钻研剩下的拦路虎——难题，心里当然从容又冷静。
	一, 做选择题时，要仔细阅读题目，一般要相信第一印象，避免在某一选择题上花太多的时间。在第一时间无法判断时，先选择一个答案，然后在该选择题做上记号，在完成整张试卷以后，专门检查有记号的问题，即使没有时间了，也不至于漏掉。在这里要特别注意，一般不要检查未做记号的问题，因为经过两个小时的考试以后，学生的判断力不见得比第一判断力要好。（时间一般为30---45分钟）
	具体方法
	1．应对客观性试题（单选）强调4个环节：
	①审题肢-------排谬法（排错法）
	②审题干-------主旨，主体，客体，谁与谁的关系
	③审题干与题肢的关系：比较正确选项的区别；带着区别对照题干主旨，挑出最佳选项。
	④反代，阅读，从中体味逻辑性和语言的通顺性。这是客观题验证的必要手段。
	注意：感觉有两个或两个以上的选项都符合题目要求怎么办? 
	首先，一定要领会题干的中心意思或总体的逻辑关系，然后比较各选项，只有与题干的中心意思联系最直接、最贴近的才能选，不能作无限延伸。特别提醒，这是最佳选择。不是所有符合题意的都可以选。如果仅仅与题干中的某一部分、某一层次的意思吻合，则不能入选。
	2正确涂卡与核查。 要注意正确标记选定的选项，要保持试卷整洁，不要乱涂乱画，防止自己最终也搞不清楚究竟选择哪个答案；选择题做完后，要及时涂卡，并进行仔细核查；核查无误后，再做非选择题。涂完放置在合适位置。如果你没有先填涂答题卡，一般来说，监考教师提醒—还有15分钟，你就必须停下来答主观题，第一要紧的是涂卡，这太重要了。
	解答选择题的注意事项
	平时要养成良好的审题做题习惯。在前面叙述中已经涉及到一些注意事项，下面再强调几点：
	1．要仔细全面阅读，不要匆匆浏览，未看清就作答。
	2．要认真推敲，不要眼高手低，急于答题。
	3．要干净利索，不要拖泥带水，纠缠不休。
	4．要把握节奏，掌握时间。
	5．要准确标记，不要乱涂乱画。
	6．要及时涂卡，检查核对。
	特别提醒，一般来讲，高考文综政史地三科总是有一科要显的难一点，这可能是由于你的学科能力，或者是高考文综平衡难度的要求，这就要求我们的学生，对这一部分特别的谨慎，对这一部分大多数题，看一遍不怎么懂，不要发慌，在看一遍，就懂了，谨慎判断，正确率会大大提高。
	二主观性试题
	在解答主观题时，一定要先读问题，再读材料，这样会让你有效提取材料中的信息，同时还能节省考试时间。另外，评分标准的弹性空间增大。即文科综合考试只存在答题效果的差异问题，而不存在答不出内容的问题。当你无法用记忆中的知识答题时，不妨像对待作文一样来对待该问题。写字速度应控制在每分钟35个以上。
	具体方法--审题要慢，入题准确，理清思路，构建答案，看分值答题，做题要快，要点化，序号化和段落化，注意书写。
	1审题（时间可稍长）
	回答论述题，关键在于审题。审清材料和题目的设问，就跟语文的作文审题一样，必须吃透题意，从材料中提取有效信息，看清题目的设问，弄清题目要求回答的中心问题究竟是什么（题目究竟考察课本中的什么基本概念、基本原理、基本观点），以免走题，造成答非所问的失误。
	审题时可以首先把该背景材料以及设问的问题分成若干个层次或几个小问题分别进行思考。
	首先，判断题型（7种）1."体现型"2."反映型" 3."为什么型"4."怎么办型"5."意义型"6."认识型"7."启示型"（遇见没见过的题型要转化为熟悉的题型。）
	其次，理清答题思路，即如何表达。
	2构思，找准答题的切入点，主体，客体，政治常识，经济常识，哲学常识。要搞清用什么原理（单个原理还是多个原理）进行表达。
	3作答
	把该回答的问题理清思路以后，就可以动笔作答了。作答时要注意把它们分成几个自然段，以要点化的方式，正确运用学科语言（学科术语），认真工整地书写（要书写清楚），将答案准确完整地表达出来。
	作答（答题）要求：
	①要有针对性，即答题思路要正确，要针对题目的设问作答，不要答非所问。
	②要有逻辑性，即先答什么，后答什么；先说那句话，后说那句话。
	③要有层次性，要层层分析，大的层次包含哪些小的层次。
	④要点化，即一定要用阿拉伯数字清楚地表明答题要点，使人对你的答案能够一目了然。
	⑤全面化，答题要点要全面，不要遗漏答题要点。
	⑥规范化，包括书写规范和语言表达规范。书写规范主要是指书写认真工整，清楚明白；用语规范是指要正确运用学科语言、学科术语来答题。我们要准确使用书本语言，学科语言（课本中的基本概念、基本原理、基本观点）答题，一般不要使用自己创造的或社会上的语言；语言要简洁明了准确。
	就政治学科而言，必须明确主观题高考答案由三大要件构成的：一是理论部分（观点、原理），要求准确选择与解题有关的基础知识，而且陈述要准确、简洁。二是论述题部分,即结合点、切入点连接问题。要求在论述过程中，把学到的知识和题目材料所对应部分挂起钩来，或是用理论分析材料，或从材料中提炼出观点，或用观点分析材料，或用材料论证观点。总之两者要有机结合，顺理成章。同时要注意做到：层次清楚，条理分明，详略得当，重点突出。三是小结部分。在小结时，或针砭时弊，或表明态度，或点明意义。这些都要从题意出发，点到为止。
	近年的高考试题中尤其是主观性命题，大多通过运用新材料，设置新情境来考查学生综合分析和解决问题能力。这就要求学生在审好题的基础上进行信息筛选，找准关键词，找出答题的依据和切入点；寻找试题与课本的关联点，作为解题的依托；要恰当地使用政治术语，条理清楚、逻辑严密。这提醒我们平时不仅要牢固掌握教材语言，而且要关心时事。因为高考答案一般由三部分语言组成：教材性语言、时事性语言、试题材料性语言。所以，要适当地记忆和掌握一些党和国家领导人的重要讲话内容以及党和国家重要会议，以便在答题时运用。
	二、如何指导学生规范答题：
	1、把答题卷上该小题规定的空格处写得比较满为好，字要写重一些（有不少卷子字写的太轻，扫描效果就不好）。
	2、写了就得分（1-2分），高考阅卷很少给0分，从有效率而言，即使该生写得不怎么对，给1-2分则为保险分。
	3、写错别字，一般不扣分，一是因为毕竟不是语文学科，且没有特别严格的要求，二是错别字一扣分，则均分就下降很多。
	4、得高分必须：a把握关键词（阅卷过程中主要看关键词采点为主）；b讲究答题的顺序，讲究格式，体现清晰的思维，而没有太多的废话；c字要写清楚（我评的14分题有学生得12-13分的，10分题有学生得10分的）。
	对不同层次学生的答题规范的要求
	（1）对好生：思维清楚逐一回答，把握好关键词，字要写好，答得感觉清楚了就可以，不一定要写得太多，平时训练中，要逐步地引导学生答案由答得多到答得清楚。
	（2）对大多数中档生
	有把握的答在前面（采点），不是特别有把握的写在后面（结合采意），适当必要时也可以摘抄材料中原文或将前面答的重复几句（可能有重复采点给分的情形），总之要写得比较满，字要写清楚，但似乎从阅卷效果而言，并不要求写得特别清楚，字写得又好又清楚，但内容不对，反而让阅卷老师一目了然，给出低分。
	（3）对学困生而言
	鼓励他们要答题，而且要写得比较满。实在不会写，可以摘抄材料中原文，如36题第1小题（10分）有不少学生摘抄原文，得6分，该题平均得分只有3.8分；我们平时考试中对学困生答题，而采用鼓励的办法，写了就得分（1分），写得较多的则给2分，这样可以提高学生答题的信心。有时候学生懂了，答题的意思到了，讲大白话也可以得分（而我们平时扣分较严），师大老师的解释是学生不容易，高考扩招了，学生就这个水平。
	（4）高考阅卷主要是采点，你写错了不扣分，写对了就给分，讲白了，就在你答案中找符合细则的，看到关键词（话）就给分，所以要教育学生答题，扬长而没必要避短，我们以往总教育学生要扬长避短，答案中不要有明显的破绽和失误，真不清楚的就不写，现在看来，要鼓励学生大胆写。
	（5）答题时如果学生写的不对就用两道线扛册掉，在下面重写，不影响评卷，如果写满了，则在空白处写上关键词（话），如36（3）WTO改革开放、主动、被动写出来，就得8分。绝不使用胶带等，用了胶带后再写的会出现重影，简直看不清楚。
	总的来说—答文总主观题的技巧是
	1．规范答题，在答题框内答题
	2．即使不会也不要空，答了就有分
	3．审题要慢，可以先看问题，带了问题仔细看懂材料，作答要细致，看清楚要几问，分别回答，暂时不会的可以留一定的空格。
	4回答要讲究要点化，序号化，段落花。
	5要点全面，思维要开，尽可能多答几点—“广度第一，深度第二”
	6要学会看该题的分值答题，看分值的大小组织答案，1-2分就一，两句话，不必要展开，可以说你答的每一句话都可能得分。
	7要合理安排好答题的空间，卷面清晰很重要。
	8要合理安排好答题的时间，要通过平时的考试去训练自己，正式大考时，监考教师会有“15分钟”的提醒，这时你如果还没有涂卡，就必须去涂卡了。一般讲，全部完成后还余10分钟左右为宜
	9真正拿捏不准的选择题，就相信你的第一感觉，不要轻易的去改答案
	10字不一定要求写的太好，但是一定要清楚工整，主观题讲究一遍完成，不要轻易划掉。
	
	
