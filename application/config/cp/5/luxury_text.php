	正确评估自己的分数，自己所处的全体的位置
	语文：语文的主观回答比较多，认为性较强，所以相比理科，语文的估分准确性比较小。即使在和标准答案核对时，并不是完全一样才会得分，主要意思，和分析角度正确即可得分。在评估语文分数时，可以采取中间值，取评分最高分合最低分的中间值。最难的，可能就是作文的得分。历年有些满分作文，并非常人成达到的，但只要你按照规律走，肯定不会失分很大。作文的估分，一般情况采取这样的模式，50分左右的基础分，这样的做分，在文体，题材，语言，错别字等基本内容上没有太大问题。剩下的10分发展分数，是看文章的角度是否具有新颖性，如果没有肯定的把握，不建议考生冒险尝试。语文是按点给分，请考生也不要过分的苛刻自己。

	数学：数学上的得分是比较客观的，根据答案和步骤按步得分基本上偏差不会很大。

	英语：想必而言，英语的客观题是最多的，选项比较多，难度最大的就是作文的估分。作文的评估上也可以按照取中间值的办法来估分，粗略的估和细致的估，然后取中间值，只要在句法，用词，单词的准确性没有什么问题，就不会失分太大。如果在写出来比较精彩的句子就会得高分，但还是建议，没有把握不要尝试，高考一分之下就是万人之上。
	物理、化学：物理和化学，是按步骤给分的，客观性也比较大，不一定是标准答案就是正确的，其他方式解答就会错，逻辑关系正确就能得分。关键在于你的答案是否完整性，是否合理。
	政治、历史：对于文科性的解答，主观性很强很难判定分数，对于政治和历史，主要是按点给分，不要拼命的死扣答案，改卷老师，也是抓关键字，没有老师会一字一句看你的答案，表述清楚，意思接近，层次分明，分数就不会太差。
	分析自己的属于哪个批次
	分批次录取，是每个省招生录取的高校较多，所有高校不可能同时录取，将不同类型的院校分期分批进行录取，由此形成录取批次。分批次录取既有利于不同类型高校招生，也有利于增加考生的录取机会。除提前批外，批次越靠后，批次录取控制分数线会逐批降低，只要正确掌握不同批次及同批次不同顺序的志愿之间的关系，前批次如果不能录取，后批次仍有机会。掌握好批次录取的机会，就能根据自己的实际情况选择合适的学校，增加自己的录取机会。
	一般来讲，高招录取批次分为提前批本科、第一、二、三批本科和高职高专院校。提前批本科录取：军事、公安、司法、安全院校(专业)等，国防生，艺术和体育类专业，部分航海类专业，经各省相关招生部门同意的其他院校(专业)。
	第一批本科录取：教育部直属院校、“211”工程院校和经省级相关招生部门同意的其他本科院校。（一本）
	第二批本科录取：一般本科院校。（二本）
	第三批本科录取：民办高校和独立学院的本科专业。（三本）
	高职高专录取：提前批高职高专录取，如公安、司法、安全院校等高职高专，艺术和体育类的高职高专；一般高职高专录取，包括高等专科学校，普通本科院校的高职高专专业，独立设置的高职院校，民办高校和独立学院的高职高专专业等。

	志愿填报模式
	目前在全国的高考中有三种志愿填报方式：考前，就是在高考没有举行之前就填报了，基本是参考一二摸成绩和平时情况而报名，现在全国就北京和上海；考后估分填报，大概有7个省市是考后分数没有出来的时候进行填报；大部分省市采用考后，分数出来后来再填报。
	1、除了需要了解一下往届高校招生录取分数外，考生最重要的问题是认真阅读招生章程。因为招生章程里面很多的细节大家可能容易忽略。作为招生章程来说是高 校方向社会承诺的方方面面的内容，如果考生不仔细琢磨的话会出现一些问题。 
	2、高考志愿填报完没法修改，被录考生不能退档补录。 
	3、平行志愿。对于整体考生是“分数优先按志愿，分数相同看科目”，而对于单个考生则是“逐个检索，一档一投、能投则投、投后不检、无投再检、检完则完”的投档原则和操作步骤。 
	填报志愿时选学校还是选专业？
