	了解大学背景
	首先要了解所报大学的招生简章，是不是符合条件，历年的报考情况，在这里给大家解释一下，经常所说的985、211是什么意思。
	“985工程”是我国政府为建设若干所世界一流大学和一批国际知名的高水平研究型大学而实施的建设工程。是以国家和行业发展急需的重点领域和重大需求为导向，围绕国家科技发展战略和学科前沿，加大学科结构调整力度，促进学科交叉，大力提高建设学科的科技创新能力和解决制约经济社会发展的重大瓶颈问题的能力。“985”大学应是重点中的重点，国家竭尽全力扶持这些高校，无论从财力还是从人力上都给予了最大的支持。考入这样的高校，自身“身价”倍增，也会前途无量。但同样，难度系数也会非常大。
	“211工程”是中国政府为了迎接世界新技术革命的挑战，面向21世纪，要集中中央和地方各方面的力量，分期分批地重点建设100所左右的高等学校和一批重点学科、专业。211工程的学校一样分数比较高，进入这样的学校一样会身价提高，未来比较平坦。

	选择合适大学
	在志愿填报的时候，有些考生或许过渡的自信，或非XX学不上，就在志愿上报一两所学校。而现在国家扩招的招生政策是分批的，增加上学机会。前一批次的落榜一般不会影响后一批次的录取，院校录取到哪个批次就依据哪个批次所填报的志愿。提前批次录取的最大好处是增加了一次录取机会。对于本科第一、二、三批的考生，尤其是第一批次的考生，一定要知道"不报白不报，报了不白报"的招生录取规则。对于后续批次志愿，如果你不报，就浪费了一次或几次可能被录取的机会。高招报考中出乎考生与家长意料之外的事是经常发生的，不要以为自己分数高，就一定会在第一批次录取，而忽略后续批次志愿的填报。
	许多考生及家长名校情结很重，一心想进名校。高考志愿填报最关键、最核心、最重要的，是所报志愿尤其是第一志愿能否抛档、能否录取。因为重点名校的第一志愿录取率一般都在百分之百，要一矢中的。如果你冒险搏高，不留余地，就会导致风险很大。如果将所选择的学校适当下降一个档次，还可将所选择的专业适当上升一个档次。将填报学校的梯度拉开，就会增加入学机会。基本按第一志愿很关键，第二志愿来保底的原则进行填报。
