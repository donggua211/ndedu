	顺序志愿：
	“顺序志愿”就是按“志愿＋分数”投档。在一个批次的录取过程中，对学校而言，所有学校只能同时同步录取同一顺序志愿的考生；对考生而言，则是所有在某一批次分数线以上的考生在同一个时间内只能是同一顺序的志愿被检索投档。这种投档办法的第一步是对所有批次分数线上的考生按其第一志愿投档，投档后所有考生的第一志愿学校都必须返回确切的投档结果（包括被学校录取、退档、分数未达学校提档线没能投出）后，第二步再将所有未录考生重新汇总，然后再同时分别投向他们所报的第二志愿学校，也就是在第一志愿学校录取完成后再进行第二志愿的投档，同时同步按志愿顺序投档录取，依次直至本批次3~4个志愿录取结束。
	按“顺序志愿”实际上就是按“志愿＋分数”投档，游戏规则明确，最大限度地保证了程序公平。但问题也是显而易见的：首先是强化了志愿的作用，因为是先按志愿分别排队，考生能否被所报志愿录取，不完全取决于考试成绩，实际上是取决于与自己分数大体相当的其他考生的志愿，如考生志愿一旦扎堆，学校提档线会大幅提高，转而次年又可能大幅下降，使得考生填报志愿，尤其是第一志愿的填报有一定的偶然性；再者就是如果考生未被第一志愿录取，当填报的第2志愿的学校已经录满其他第1志愿的考生时，该生最后的录取结果可能使他感到很大的心理落差。实行这种录取投档办法的省份已经较少。 
	　 　对于实行按“顺序志愿”（即“志愿＋分数”）录取投档办法省份的考生，在填报志愿时要注意以下问题。 

	第一志愿是关键 
	首先分析一下所报考院校的历年情况。如果考生所报的第一志愿学校在考生所在省份近2~3年录取分数大体稳定，则考生分数最好达到该校上年度录取分数范围的中间段才较有把握，而且专业也有一定选择余地，如果仅仅达到分数范围的下端，选报时则要慎重。如果考生选报的学校近2~3年在该省录取分数“忽高忽低”，则考生分数最好达到其录取分数范围的上端，才较有把握。从录取结束后考生的心理状态看，学校报低了，但专业更容易满足，特别是对于录取到一般本科及高职院校来讲，在就业压力较大时专业尤显重要，因此这样的录取结果只是“小遗憾”；如果志愿报高了，第一志愿学校未能如愿则最后的录取结果可能是“大遗憾”。 
	第二志愿应是“保底”志愿 
	按照“顺序志愿”的投档办法，考生填报的第2志愿学校必须是第1志愿生源不足的学校，绝不能觉得自己的分数比该校上年实际录取分数高出很多就可将该校做为第2志愿学校。因为有不少学校虽然录取分数在批次内不是很高，但第1志愿生源充沛，录取的基本都是第1志愿考生。那么如何判定一所学校上年录取的学生第1、第2志愿学生各占多少比例呢？有的省市提供了详尽的录取统计资料，可以查到，有的省市的录取统计资料只是给出了各校录取分数，并未给出分志愿顺序的录取人数，这时就要直接咨询学校了。只有将第1志愿生源不足的学校做为自己的第2志愿学校才有可能被录取，才能“保底”。目前实行这种投档办法的省份，一般而言，第1志愿录取率可达75%左右，第2志愿录取率可达15%左右，第2志愿录取完成后总体上计划可完成90%以上，因此，考生应将第2志愿定位在自己可接受的最后的“保底”志愿，在“可能”被录取的学校中慎重选择。一旦前两个志愿未能录取，甚至分数高出批次线不少，尽管录取不应有什么问题，但最后的录取结果可能会很不“如意”。 
	志愿间及批次间的填报策略
	在一个批次内，如果可选报的第1志愿学校，从上2年实际录取分数看，只有50~60%的把握，则一定要报好第2志愿，选择第1志愿生源不足的并且是自己可以接受的学校做为第2志愿，才有可能被录取，也才有可能得以“保底”。如果找不到很合适自己的第2志愿学校，则不如第1志愿就选择有70~80%录取把握的学校。至于批次间的填报策略，主要指分数达到本科重点（一表）线以上不多，填报重点批次院校也可以适当“高报”，选择只有50%把握的学校作为第1甚至第2志愿，如不能录取，则放弃重点批次，（不服从调剂，甚至不填满一批志愿），用相对较高的分数到二批本科选择自己理想的学校做为第1志愿


	平行志愿
	普通类考生可以在提前及本科各批次中分别填报A、B、C 3个平行院校志愿，在专科各批次中分别填报A、B、C、D、E 5个平行院校志愿。各院校志愿之间是平行关系。另外，每个院校志愿下可填报6个专业志愿和1个专业服从志愿。按“分数优先，遵循志愿”的原则投档。
	一是志愿并列，考生所填报的多个院校志愿属于并列关系，即在同一投档时间段，考生所填院校均有可能被检索；
	二是分数优先，在检索考生填报的院校志愿前，首先要将所有考生分文史、理工类按投档分(高考总分加政策照顾分)从高分到低分进行位次排列，投档检索时，先从最高位次考生志愿检索，再依次检索以后位次的考生志愿；
	三是遵循志愿，考生所填报的多个院校志愿虽然属于并列关系但是院校志愿栏目之间仍有自然排列顺序，检索时，是按考生填报排列的院校顺序依次进行的，当考生的投档分符合首先被检索到的院校投档线时，该生即投到该院校，由高校择优录取，考生档案一旦投出，即不再检索该生的其它志愿；
	四是一轮投档，因考生所填报的志愿是平行的，故投档检索只能进行一轮，如果考生因几个平行志愿均未满足院校条件而未被投档，或投档后由于某种客观原因被退档时，可以参加同批次征求志愿或下一批次的投档检索。
	需要提醒考生的是：“平行志愿”不是“平等志愿”也不是“平行录取”。考生填报的平行志愿有自然顺序，并不是只要成绩达到所填报的4个平行志愿院校录取条件，就可能会被4所平行志愿院校同时录取。而是只要考生档案投到一所志愿高校后，其他志愿就不再检索，对每个考生而言投档录取机会仍然只有一次。
	一个位次对应一个考生。考生的投档分是考生高考成绩总分加上固定的政策性照顾加分后的分数(是判断考生是否向高校投档的基本依据)。比如，某考生高考总分是590分，如果他是省优秀学生，按我省政策，他可以享受20分政策性照顾加分，他的投档分就是610分。
	平行志愿是从高分到低分对考生逐个检索，当遇到同科类同批次同分考生时，计算机是无法判断先检索谁后检索谁，因此必须分科类对同分考生按照事先确定的排序规则排出一个位次，一个位次对应一个考生。位次是惟一的，计算机可以按位次对考生逐个检索。
	那么平行志愿是不是只对高分考生有利呢？省高招办专业人士指出，平行志愿投档录取模式的意义在于，它对于不同批次不同分数的考生都增加了选择面，每个考生可以同时填上几所梯度层次的高校，特别是每一批次都有其相对高分考生，选择面和受益面更明显，因此平行志愿对高分考生有利的同时，也一样增加了其他考生的投档机会。

	平行志愿风险注意规避
	目前平行志愿仍有一些风险，需要引起考生和家长的重视。
	由于平行志愿投档录取模式是采取一次性投出，一轮投档后不可能补投。考生被投档后，如果成绩相对较低、专业不服从调剂、不符合特殊专业的体检要求、不符合某些专业的单科成绩要求等，高校就可能将档案退回。而一旦出现退档，考生只有参加征求志愿填报或下一批次录取。
	同时，考生定位不恰当、志愿梯度不合适也是造成落榜的重要因素。如考生填报的4个平行志愿都过高，投档分未能达到院校的投档线，考生档案就不可能被投出。如果考生填报的平行志愿之间没有适当的“梯度”，则该考生被投档的机会将大为减少，相当于自己把原有的4次机会变成一次机会，那样变成了如果A志愿不能投出，而B、C等后续平行志愿院校比A志愿院校要求还高，则后面的平行志愿显然也都无法投档。
	设置院校平行志愿后降低了考生填报志愿的风险，但并不能完全化解现行录取体制下填报高考志愿的所有风险。风险不是平行志愿带来的，在现行录取体制下，院校调档数大于其招生计划数（高校调档比例在1∶1.2范围内），必然有一定数量的考生在公平竞争、全面考核、综合评价、择优录取后自然落榜。因此考生填报志愿仍然要准确定位，统筹兼顾，慎重选择。并要及时了解省市县招办发布的有关信息，确保能在规定的时间规定的地点填报征求平行院校志愿。 

	风险一：专业志愿不愿意调剂
	很多考生和家长认为平行志愿就是“保险志愿”，只要考分考高了，学校可以随便挑，这是完全错误的。高分考生仍然存在落榜可能。 
	假设某考生一本填报的4个志愿依次为复旦大学、上海财大、华东政法、上海大学，被投档到复旦大学，但复旦大学根据招生计划数，该考生因为排名靠后，同时不愿接受专业调剂，而没有被录取而被退档，考生就直接落到了征求志愿档，而无法投档到填报的其他三所院校。只要考生在每批次的首次投档中被退档，就会出现这样的情况。 
	-建议：考生被高校退档，主要原因是考生。填报专业不当，如考生填报的专业扎堆，又不愿意接受专业调剂等。其次，还有可能是考生自身的条件不符合专业录取要求的限制。因此，考生填报志愿时，要正确评估自己的综合实力，合理搭配平行志愿。 

	风险二：定位不准排序不慎重
	考前估分本身就存在一定风险。其次，虽然一二批次的4个志愿和三批次的8个志愿全是1志愿，但是在操作上，A、B、C、D志愿是有顺序的。如某考生填报了A、B、C、D4所院校，投档时先看A志愿院校的投档情况，如果A院校额满，再向B志愿院校投档，如B院校额满，再投向C志愿院校……依次类推。 
	有的考生不会准确定位，为提高命中率选择录取分远低于自己高考成绩的院校，或者选择自己高考成绩达不到录取分的院校，都会产生录取结果不理想的后果。 
	-建议：一是准确把握自己的学业水平，具体要参照考生的成绩在群体中的位置。从考生家长来说，要多和孩子的班主任沟通；二是要准确估计考生的考试心理状态。有的考生平时看似不用功，但考试时往往能超常发挥，这就是比赛型的考试心理素质。 

	风险三：专业和院校难两全其美
	 在高考填报志愿中，究竟是选学校还是选专业是考生家长最难把握的。今年上海实行平行志愿填报，在选专业还是选学校的博弈中，这一难题将更为显现。这是因为，院校的投档比例降低，专业的匹配率也会降低。 
	 招办老师告诫考生，从该校往年从实施平行志愿的地区招生情况来看，考生考分情况非常扁平：最高分和最低分一般只相差了10分左右。各个专业的录取分相差不大。因此，如果学生同意调剂专业志愿，则被录取的可能性就越大。而那些不愿意调剂专业志愿的学生则被退档的可能性越大。 
	-建议：在平行志愿中，如果选学校，就要做好牺牲专业的准备，如果选择专业，则要做好学校要降一档次的准备。考生要自己掌握其中的平衡问题，因为好的大学里面也有弱的专业，一般的大学里面有强的专业。 
	由于在平行志愿中，一、二、三批次填报的志愿都是第一志愿，这样考生在填报前，要对每所院校各个专业的情况都有所了解，而且究竟是选专业还是选学校，在一二三批次志愿顺序的填报中也要体现出来。考生和家长在填报志愿前就应该有所规划。 

	风险四：志愿之间坡度不合理
	若考生在填报时没有兼顾自身的实力，填报的志愿偏高，或没有梯度等，也可能出现高分落榜。
	-建议：为增加录取机会，提高志愿的有效性，同一批次志愿中，院校之间要有坡度。要把握这一坡度，要清楚了解院校往年的录取平均分，它可以帮助考生大致了解该学校在考生中的定位并把自己的实际学业成绩和之相对应。一般四个志愿的坡度应根据“冲一冲”、“稳一稳”、“保一保”、“垫一垫”来设计。 

	风险五：院校考分可能整体拔高
	由于今年高考实施平行志愿，很可能产生各院校录取分数线整体拔高的情况。另一方面，由于今年高考填报志愿不强求考生必须每一批次上填报一所外地院校，考生如报考外地院校反而拥有明显优势。 
	-建议：考生要充分运用好鼓励填报外地院校的政策，踊跃报考外地院校。今年，对第一、二、三批次填报外省市院校志愿的考生，当规定分数线上平行志愿投档后仍有缺额，可在征求志愿前，在征得外省市招生院校同意的情况下，对一本、二本、三批高职(专科)线下20分之内、填报有志愿的考生，加20分实行平行志愿投档办法，录取与否由外省市招生院校决定。此外，外省市院校降分投档也将继续实行。

	省高招办相关负责人建议考生注意规避风险，在专业选报上恰当定位，并根据情况选择“服从专业调剂”；身体有缺陷的考生最好填报身体不受限制的专业；高校有些专业是有特殊要求的，考生要仔细阅读招生章程，看高校哪些专业有特殊限制，以提高自己的录取机会；量力而行，选择适合自己的高校，院校间一定要有适当的梯度，后面的志愿院校的投档分一定要比前面志愿院校的低。
	把握顺序记住参考口诀
	从理论上讲，实行平行志愿后，几所好大学可以同时填报了，但考生和家长必须注意顺序。因为平行志愿投档，是按考生平行志愿所列各院校的先后顺序依次检索的，一旦符合条件，档案即投向先检索的高校。如果平行志愿的顺序不合理或所填报院校近年录取分数线相对于自己的分数均较高，也可能导致考生与较好院校失之交臂，甚至无法出档。
	如何把握好“平行志愿”之间的顺序？请家长和考生记住参考“口诀”——冲一冲、稳一稳、保一保、垫一垫。即把握好冲、稳、保、垫之间的梯度关系。
	在填报平行志愿时，考生不妨将自己有希望“跳一跳”够得着的理想学校作为志愿A，将符合自己成绩水平的“对口”学校作为志愿B，志愿C填一所“保守”学校，志愿D则填“垫底”学校。这样的话，几所学校就形成了梯度配置的顺序，可避免“高分低就”带来的懊悔，也防万一没达到较好院校的投档分，也有相应的院校可读，不至于落到低批次院校或落榜。
