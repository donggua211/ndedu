	九、 学习别人     
	孔子曰：“三人行必有我师”，别人总会有比自己强的地方。别人做了什么自己不做的事，就应该想想，我这样做对我有没有益处，这是他成功的秘诀吗？这是我学习别人的主要方法。
	不要避免与比自己成绩好的人说话，他们有很多地方是你所没有的。应该向他们学习。必要的时候厚脸皮一点，向比自己强的人多请教一些东西，探讨一些学习的方法。这些人往往是能够给你最大帮助的人，他们的无意的一句话，也许会使你茅塞顿开。这都说不准的。比你成绩差的人未必处处比你差，他们之中也有你学习的地方，你必须分清什么样的是造成他们不如你的原因，就不要学。而提炼出来他们身上的精华。
	课堂教学相适应的学习方法，就是预习，听课，复习，作业等的基本方法，这也是其他学科的基本方法。
	一． 预习的方法
	预习是上课前对即将要上的数学内容进行阅读，了解其梗概，做到心中有数，以便掌握听课的主动权。由于预习是学生独立学习的常尝试，对学习内容是否正确理解，能否把握其重点，关键，洞察到隐含的思想方法等，都能在听课中得到检验，加强或矫正，有利于提高他们的学习能力和养成自学的习惯，所以它是数学学习中的重 要一环。
	预习的方法，除了回忆或温习学习新内容所需的旧知识（或预备知识）外，还应该了解其基本内容，也就是知道要讲些什么，要解决什么问题，采取什么方法，重点关键在哪里等。预习时，一般采用边阅读，边思考，边书写的方式，把内容的要点，层次，联系划出来或打上记号，写下自己的看法或弄不懂的地方与问题，最后确定听课时要解决的主要问题或打算，以提高听课效率。在时间的安排上，预习一般放在复习和作业之后进行，即做完功课后，把下次课要学的内容看一遍，其要求则根据当时具体情况灵活掌握。如果时间允许，可以多思考一些问题，钻研得深入一些，甚至可做做练习题或习题；时间不允许，可以少思考一些问题，留给听课去解决的问题就多一些，不必强求一律。
	二． 听课的方法
	在听课时，一方面理解教师讲的内容，思考或回答教师提出的问题，另一方面还要独立思考，鉴别哪些知识已经听懂，哪些还有疑问或有新的问题，并勇于提出自己的看法。如果课内一时不可能解决，就应把疑问或问题记下，留待课后自己去思考或请教老师，并继续专心听老师讲课，切勿因一处没有听懂，思维就停留在这里，而影响后面的听课。一般，听课时要把老师讲课的要点，补充的内容与方法记下（也就是记笔记），以备复习之用。
	三． 复习的方法
	复习就是把学过的知识再进行学习，以达到深入理解，融会贯通，精练概括，牢固掌握的目的。复习应与听课紧密衔接，边阅读教材边回忆听课内容或查看课堂笔记，及时解决存在的知识缺陷与疑问。对学习的内容务求弄懂，切实理解掌握。如果有的问题经过较长时间的思索，还得不到解决，则可与同学讨论或请老师解决。
	四． 作业的方法
	学习往往是通过做作业，以达到对知识的巩固，加深理解和学会运用，从而形成技能技巧，以及发展智力与数学能力。由于作业是在复习的基础上独立完成的，能检查出对所学知识的掌握程度，能考察出能力水平，所以它对于发现存在的问题，及时采取措施加以解决，有着重要的作用。一般，当做作业感到困难，或做错的题目较多时，往往标志着知识的理解与掌握上存在缺陷或问题，应引起警觉，需及早查明原因，予以解决。    
	通常，作业表现为解题，解题要运用所学的知识和方法。因此，在做作业前许要先复习，在基本理解与掌握所学教材的基础上进行，否则事倍功半，花费了时间，得不到应有的效果。解题，要按一定的程序，步骤进行。首先，要弄清题意，认真读题，仔细理解题意。如哪些是已知的数据，条件，哪些是未知数，结论，题中涉及到哪些运算，它们相互之间是怎样联系的，能否用图表示出来等，要详加推敲，彻底弄清。其次，在弄清题意的基础上，探索解题的途径，找出已知与未知，条件与结论之间的联系。回忆与之有关的知识和方法，学过的例题，解过的题目等，并从形式到内容，从已知数，条件到未知数，结论，考虑能否利用它们的结果或方法，可否引进适当辅助元素后加以利用；是否能找出与该题有关的一个特殊问题或一个一般问题或一个类似问题，考察解决它们对当前问题有什么启发；能否把条件分开，一部分一部分加以考察或变更，再重新组合，以达到所求结果等等。这就是说，在探索解题过程中，需要运用联想，比较，引入辅助元素，类比，特殊化，一般化，分析，综合等一系列方法，并从解题中学会这一系列探索的方法。在探索解题方法中，如何灵活运用知识和方法具有重要意义，也是培养能力的一个极好机会。第三，根据探索得到的解题方案，按照所要求的书写格式和规范，把解题过程叙述出来，并力求简单，明白，完整。最后，还要对解题进行回顾，检查解答是否正确无误，每步推理或运算是否立论有据，答案是否详尽无遗；思考一下解题方法可否改进或有否新的解法，该题结果能否推广等，并小结一下解题的经验，进而发展与完善解题的思想方法， 总结出带有规律性的东西来。
	
