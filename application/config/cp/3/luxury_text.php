	高中学习与小学学习有很大的不同，高中生他们可塑性大，既是掌握基础知识、基本技能的最佳时期，又是为今后发展创造条件的重要时期。与小学阶段的学习相比，表现出以下明显的差异：
	1、学习内容逐步深化、学科知识逐步系统化
	学习的课程门类逐渐增加，内容也逐步加深
	进入高中以后，学习的内容发生了明显的变化。学习的课程门类逐渐增加，内容也逐步加深。语文、数学、英语这些小学曾学习过的课程，由直观的、感性的、零碎的知识点变成了更为完整、系统的知识体系，并更加突出能力要求；同时，物理、化学等课程相继开设，历史、地理、生物等人文社科知识也成为重要的学习内容。这些学科知识对于初中生来说，都是必需的文化素质积累，这就使高中的学业负担客观上大大增多了。老师的教学也越来越注重传授知识的严密性和注重学生思维方法、思维能力的培养，除要求学生识记大量的定义、原理等知识点外，更重要的是培养学生掌握运用知识的能力。
	2、 学习成绩分化日趋激烈
	高中生的学习成绩波动很大，同时出现激烈的分化
	学习量的增加和内容的不断加深，加上高中学生心理的波动和生理的变化，使得高中生的学习成绩波动很大，同时出现激烈的分化。主要表现在以下几个方面：
	(1)进入高中以后继续保持领先的情况大大减少；相反，有些小学时被认为成绩不好的学生，往往后来居上成为学习冒尖者。而初中阶段的学习成绩却与高中学习呈明显相关。 
	(2)对学生逻辑思维能力要求越来越高，智力在学习中的作用也表现得越来越突出，这时学习开始出现好的更好，差的更差，好与差的差距被越拉越大的状况。
	(3)学习成绩与付出的工夫所呈现的差异。学习优秀的学生由于能够合理地安排时间，方法得当，事半功倍，学习往往显得轻松自如而依旧学有余力；学习较差的学生穷于应付，事倍功半，却学得越来越吃力，学习变成了沉重的负担。这种对待学习是否轻松的状态，是判断学生学习潜力的重要依据。
	3、学生在学习中的自主能力日显重要  　　对高中学生的指导更多的应侧重于学习方法和学习意志品质的培养  因此，自学能力的强弱对学习成绩的影响明显增强，学习依赖性强的学生成绩往往每况愈下。家长应该及时指导学生充分利用小学阶段已经形成的良好学习习惯，使他尽快适应初中学习的要求，并帮助其形成初中阶段相对独立的学习能力。对学生的指导更多的应侧重于学习方法和学习意志品质的培养。
	4、学习的自觉性和依赖性、主动性和被动性并存  　　 相对来说，高中学生的学习自觉性和主动性有所增强，但是，在这方面学生之间存在显著的差异。许多学生的自觉性和主动性还不能持久保持，教师和家长的管理稍有松弛，就会无法自制，把学业抛至脑后，从而导致学习成绩的下降。随着学生年龄的增长、心理的变化，电脑游戏、武侠小说、通俗言情小说，乃至有些不健康的读物都会对学生产生极大的诱惑，成为影响学习的因素。有些学生自制能力比较脆弱，还会沉迷其中不能自拔，严重影响学习。自觉性、主动性比较强的学生就能保持相对持久和稳定的学习状态。
	(二) 初中学习环节
	俗话说，“习惯成自然”，良好的学习习惯对学习有着重要的促进作用。比如：课前预习新课的习惯，可以在教师教授新课之前大致了解课程内容，有助于把握重点带着问题听课，从而提高课堂学习的质量；作业认真书写的习惯，不仅可以保证作业的美观整洁，提高作业的质量，还能够培养一丝不苟的严谨作风。反之，不良的习惯也会成为学习进步的绊脚石，不少成绩比较差的学生，脑子都不笨，但往往上课心不在焉、作业马马虎虎、做事丢三拉四。
	1、 抓住课堂四十五分钟，学会听课
	听课也有不少学问。学会听课，对高中的学习进步至关重要
	课堂学习是学习的最主要环节，四十五分钟课堂学习效益的高低，某种程度上决定着学生学习成绩的好坏。
	也许有的家长和学生会想，每个人都有一双耳朵，听课谁不会呀。其实不然，听课也有不少学问呢。学会听课，对初中生的学习进步至关重要。
	首先，要集中注意听。心理学研究表明：注意能够帮助我们从周围环境所提供的大量信息中，选择对当前活动最有意义的信息；同时，使心理活动维持在所选择的对象上，还能使心理活动根据当前活动的需要作适当的分配和调整。所以，注意对于学习尤为重要。集中注意、专心致志才能学有所得；心不在焉、心猿意马往往一无所获。
	其次，要带着问题、开动脑子听。有些同学听课不善于开动脑子积极思维，看似目不转睛，但一堂课下来心中却不留痕迹。俗话说：疑是一切学习的开始。带着问题听课，就能使听课有比较明确的目标和重点，增强听课的针对性，从而提高课堂学习效率；带着问题听课，还能促使自己积极动脑，紧跟老师的教学节奏，及时理解和消化教学内容。
	再次，要积极举手发言，认真做好笔记。教与学应是双向交流、互相促进的。学生在课堂中，应该积极主动地参与教学。积极举手发言就是一种参与，它既能较好的促使自己专心听课、动脑思维，还能锻炼语言表达能力。
	“不动笔墨不读书”、“好记性不如烂笔头”，都是说边学习边动笔的好处。笔记不仅是学习新知识的方法，也是复习旧知识的依据，同时我们还可以从笔记中发现新的问题。很多家长感到对孩子在学校里的学习无从了解和把握，其实，每天查看一下他们的课本和笔记，就是一种好方法。
