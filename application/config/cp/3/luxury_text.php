	七：避免老误区一是力避病句和错别字。二要力避隐晦，巧妙点题。三要力避知识性错误，不要张冠李戴。四要力避不规范答题。网上阅卷，阅卷老师无法调阅非自己阅读权限外的试题，必须在规定的区域内答题，过去那种“下面见某页”的答题习惯必须纠正。
  
	数学 
	通过历年高考的试题的启发，我们总结出高考数学的答题技巧和策略。
	一、历年高考数学试卷的启发
	1.试卷上有参考公式，80%是有用的，它为你的解题指引了方向；
	2.解答题的各小问之间有一种阶梯关系，通常后面的问要使用前问的结论。如果前问是证明，即使不会证明结论，该结论在后问中也可以使用。当然，我们也要考虑结论的独立性；
	3.注意题目中的小括号括起来的部分，那往往是解题的关键；
	二、答题策略选择
	1.先易后难是所有科目应该遵循的原则，而数学卷上显得更为重要。一般来说，选择题的后两题，填空题的后一题，解答题的后两题是难题。当然，对于不同的学生来说，有的简单题目也可能是自己的难题，所以题目的难易只能由自己确定。一般来说，小题思考1分钟还没有建立解答方案，则应采取“暂时性放弃”，把自己可做的题目做完再回头解答；
	2.选择题有其独特的解答方法，首先重点把握选择支也是已知条件，利用选择支之间的关系可能使你的答案更准确。切记不要“小题大做”。注意解答题按步骤给分，根据题目的已知条件与问题的联系写出可能用到的公式、方法、或是判断。虽然不能完全解答，但是也要把自己的想法与做法写到答卷上。多写不会扣分，写了就可能得分。
	三、答题思想方法
	1.函数或方程或不等式的题目，先直接思考后建立三者的联系。首先考虑定义域，其次使用“三合一定理”。
	2.如果在方程或是不等式中出现超越式，优先选择数形结合的思想方法；
	3.面对含有参数的初等函数来说，在研究的时候应该抓住参数没有影响到的不变的性质。如所过的定点，二次函数的对称轴或是……；
	4.选择与填空中出现不等式的题目，优选特殊值法；
	5.求参数的取值范围，应该建立关于参数的等式或是不等式，用函数的定义域或是值域或是解不等式完成，在对式子变形的过程中，优先选择分离参数的方法；
	6.恒成立问题或是它的反面，可以转化为最值问题，注意二次函数的应用，灵活使用闭区间上的最值，分类讨论的思想，分类讨论应该不重复不遗漏；
	7.圆锥曲线的题目优先选择它们的定义完成，直线与圆锥曲线相交问题，若与弦的中点有关，选择设而不求点差法，与弦的中点无关，选择韦达定理公式法；使用韦达定理必须先考虑是否为二次及根的判别式；
	8.求曲线方程的题目，如果知道曲线的形状，则可选择待定系数法，如果不知道曲线的形状，则所用的步骤为建系、设点、列式、化简（注意去掉不符合条件的特殊点）；
	9.求椭圆或是双曲线的离心率，建立关于a、b、c之间的关系等式即可；
	10.三角函数求周期、单调区间或是最值，优先考虑化为一次同角弦函数，然后使用辅助角公式解答；解三角形的题目，重视内角和定理的使用；与向量联系的题目，注意向量角的范围；
	11.数列的题目与和有关，优选和通公式，优选作差的方法；注意归纳、猜想之后证明；猜想的方向是两种特殊数列；解答的时候注意使用通项公式及前n项和公式，体会方程的思想；
	12.立体几何第一问如果是为建系服务的，一定用传统做法完成，如果不是，可以从第一问开始就建系完成；注意向量角与线线角、线面角、面面角都不相同，熟练掌握它们之间的三角函数值的转化；锥体体积的计算注意系数1/3，而三角形面积的计算注意系数1/2 ；
	与球   球有关的题目也不得不防，注意连接“心心距”创造直角三角形解题；
	13.导数的题目常规的一般不难，但要注意解题的层次与步骤，如果要用构造函数证明不等式，可从已知或是前问中找到突破口，必要时应该放弃；重视几何意义的应用，注意点是否在曲线上；
	14.概率的题目如果出解答题，应该先设事件，然后写出使用公式的理由，当然要注意步骤的多少决定解答的详略；如果有分布列，则概率和为1是检验正确与否的重要途径；
	15.三选二的三题中，极坐标与参数方程注意转化的方法，不等式题目注意柯西与绝对值的几何意义，平面几何重视与圆有关的知积，必要时可以测量；
	16.遇到复杂的式子可以用换元法，使用换元法必须注意新元的取值范围，有勾股定理型的已知，可使用三角换元来完成；
	17.注意概率分布中的二项分布，二项式定理中的通项公式的使用与赋值的方法，排列组合中的枚举法，全称与特称命题的否定写法，取值范或是不等式的解的端点能否取到需单独验证，用点斜式或斜截式方程的时候考虑斜率是否存在等；
	18.绝对值问题优先选择去绝对值，去绝对值优先选择使用定义；
	19.与平移有关的，注意口诀“左加右减，上加下减”只用于函数，沿向量平移一定要使用平移公式完成；
	20.关于中心对称问题，只需使用中点坐标公式就可以，关于轴对称问题，注意两个等式的运用：一是垂直，一是中点在对称轴上。
	四．每分必争
	1.答题时间共120分，而你要答分数为150分的考卷，算一算就知道，每分钟应该解答1分多的题目，所以每1分钟的时间都是重要的。试卷发到手中首先完成必要的检查（是否有印刷不清楚的地方）与填涂。之后剩下的时间就马上看试卷中可能使用到的公式，做到心中有数。用心算简单的题目，必要时动一动笔也不是不行（你是写名字或是写一个字母没有人去区分）。
	2.在分数上也是每分必争。你得到89分与得到90分，虽然只差1分，但是有本质的不同，一个是不合格一个是合格。高考中，你得556分与得 557分，虽然只差1分，但是它决定你是否可以上重本线，关系到你的一生。所以，在答卷的时候要精益求精。对选择题的每一个选择支进行评估，看与你选的相似的那个是不是更准确？填空题的范围书写是不是集合形式，是不是少或多了一个端点？是不是有一个解应该舍去而没舍？解答题的步骤是不是按照公式、代数、结果的格式完成的，应用题是不是设、列、画（线性归化）、解、答？根据已知条件你还能联想到什么？把它写在考卷上，也许它就是你需要的关键的1分，为什么不去做呢？
	3.答题的时间紧张是所有同学的感觉，想让它变成宽松的方法只有一个，那就是学会放弃，准确的判断把该放弃的放弃，就为你多得1分提供了前提。
	4.冷静一下，表面是耽误了时间，其实是为自己赢得了机会，可能创造出奇迹。在头脑混乱的时候，不防停下来，喝口水，深吸一口气，再慢慢呼出，就在呼出的同时，你就会得到灵感。
	5.题目分析受挫，很可能是一个重要的已知条件被你忽略，所以重新读题，仔细读题才能有所发现，不能停留在某一固定的思维层面不变。联想你做过的类似的题目的解题方法，把不熟悉的转化为你熟悉的也许就是成功。
	6.高考只是人生的重要考试之一，其实人生是由每一分钟组成的。把握好人生的每一分钟才能真正把握人生。其实真正的高考是在你生活的每1分钟里。

	英语
	高考英语要想取得高分，除了平时的积累和努力，我们还需要做到以下3点：
	准备工作要充分
	带好2B铅笔两支以备用，质量好的软橡皮；
	涂答题卡必须反复核对，绝不出错。涂的过程中不要空格，以免错位，造成重大损失。
时间控制有原则
	每年高考下来，许多考生反映时间不够。如何在120分钟有效地完成，我们要严格地控制时间。
	1、一般分三块：第一块，听力20分钟，单项填空10分，共30分钟。（听力的时间没法改变。但单项的时间我们可以灵活处理，比如发下试卷后，在做听力之前我们可以浏览试题，这时不能动笔，可以用手指做标记。听力完后，马上转涂在答题卡上。可以把这个节约的时间用在后面的写作或阅读上）
	第二块，完型填空20分钟，阅读理解35分钟，共55分钟。
	第三块，改错10分钟，写作20分钟，共30分钟。（为了保证质量，写作最少不低于20分钟）
	还剩下5钟用于检查核对。有些考生做题过希望多留时间来检查，往往得不偿失。
　　2、有的考生习惯把阅读理解全部或部分放到最后做，这也是可以的，但在时间安排上要有原则：听力、单项、完型填空应该在50分钟内完成；改错、写作所需的30分钟不可少。
　　做题技巧有讲究
	高考英语要想取得好成绩，答题技巧很重要。答题顺序不一定按题号进行。可先从自己熟悉的题目答起，从有把握的题目入手，使自己尽快进入到解题状态，产生解题的激情和欲望，再解答陌生或不太熟悉的题目。若有时间，再去拼那些把握不大或无从下手的题。这样也许能超水平发挥。
	一、单项选择  
	虽然在整篇试卷中占的分值最少，但此题做得是否顺利，直接影响后面的做题心情。而近年来单项填空的命题特征：知识覆盖面广、境地位突出、交际性原则明确、试题题干新颖、迷惑性较大、灵活性较高       
	所以要取得此题的高分，我们要掌握一定的解题技巧：还原法，排除思维定势、克服汉英差异、注意题干惯性 、区分形近义近词、 弄清语言环境
	（一）、还原法
	题干以省略句，疑问句，被动句，倒装句，强调句或使用从句等形式，避开考生所熟悉的陈述句结构，从而达到加大难度。我们以例题来说明：
	<pre>Eg: To all of you _____ the honor for the success． 
        A．belongs to          B．belong to 
        C．belongs              D．belong </pre>
	分析：一看这个题，我们就知道考的是belong to 这个短语。做题的关键在于断定主语，有的同学认为all of you 是主语，就毫不犹豫地选了B. 其实，这是个倒装句，还原后为：The honor for the success belongs to all of you.答案是C。
	解题决窍：
	将不熟悉的疑问句，倒装句，被动句，强调句等还原为熟悉的陈述句；也可将残缺的部分补出或将多余的部分删除。 
	（二）、除思维定势
	利用思维定势的影响来设题，这是最容易让我们上当的题。因为我们背记了许多语法规则，词汇，词的固定搭配和句子结构等，做题时只注意这些熟悉的语法规则，结构和局部固定搭配，往往在没有完全弄清整个题干意思是什么就作出了选择，结果当然出错。
	Eg: 1.I’m sorry I can’t help _____ the floor of the classroom.
		A. sweeping     B. swept     C. to sweep   D. to sweeping
	分析：can’t help doing（情不自禁做什么）是我们熟记的短语。但注意的是I’m sorry，不能帮忙，要用can’t help to do sth.
	2.Do you have any idea of the reason _____ he referred to？ 
    A．that B．where C．why D．when 
	分析：一些同学误选了why . reason 做先行词，引导词一般用why。但我们要注意到从句中缺宾语，why 不能充当宾语。应该选A。
	解题决窍：
	正确理解句意，避免定势思维。在学习过程中，尤其是复习阶段，我们应就一些常见的、重要的词、词组及句型加以反复训练，以加深印象，以避免思维定势的干扰。
	（三）、克服汉英差异
	英汉在表达习惯，思维方式等方面具有很多不同，命题者常利用汉英差异出题。如汉语的“参加”在许多情况下都适用，如参加会议，参加考试，参加解放军等，而在英语中则因不同的宾语用不同的动词，如: take part in the celebration, attend a meeting, join the army, take an exam, join in a discussion 
	例如：.– It’s getting late. I\'m afraid I must be going now.
	　     - OK. ______ .（2004全国卷）
	　    Ａ．Take it easy    B. Go slowly       C.   Stay longer    D. See you
	分析：A, B, C 都是中国的汉语表达。按英语的表达习惯应选D.
	解题决窍：
	留心英汉表达差异 ,平时要多读﹑多比较﹑多归纳，尽量避免Chinglish。
	（四）、注意题干惯性
	此种类型的题多以对话形式出现，我们答题往往会借助原题干的动词时态和结构等来判断选项，结果造成错选。
	Eg:. --You haven\'t been to Beijing, have you?
	--_____.How I wish to go there!
	A. Yes, I have    B. Yes, I haven\'t    C. No, I have  D. No, I haven\'t
	分析：从后面的How I wish to go there!，知道是没有去过北京。根据英语表达，应该选D（是的，我没有去过）。
	解题决窍：
	1）补全对话；
	2）分清角色。由于这种试题多出现在对话题中，答语往往较简洁，多有省略，拿不准时，应将答语补充完整。另外要分清问答语气不同所使用的情态动词也不同，注意汉英不同习惯的表达。
	（五）、分形近义近词
	把相似、相近的词语或结构放在一起作为干扰选项，若我们基础知识掌握不牢就难于区分。例如：
	1. The films made by Disney ______ all over the world.
	     A．are used to show           B．are used to showing 
	     C．used to be shown          D．used to show 
	分析：此题是检测be used to do, be used to doing, used to do 的用法。句子缺谓语动词，而且要用被动，选C。
	解题诀窍
	1）明确词意   所选答案要符合句子意思；
	2）归纳比较 平时要特别重视收集归纳比较同义词、近义词和相似词的异同，牢记它们的用法。
	（六）、弄清语言环境
	命题人在题干中不明确将语言时间或空间背景标示出来，而是比较巧妙地隐含在句子中，稍不注意就会错选。例如：
	－Hey, look where you are going?
	－Oh, I\'m terribly sorry.＿＿＿．
	　    A. I‘m not noticing　　　　 B. I wasn\'t noticing
	　    C. I haven\‘t noticing　　　  D. I don\'t noticing
	分析：“I”回答的时候已经注意了。是刚才“你“说的时候没注意到。选B。
	解题诀窍
	1）身临其境，揣摩意图 特别是对付考查时态的试题更要将自己置身于所提供的背景之中，同时注意揣摩命题者的意图；
	2） 认真分析题干里所隐藏的信息  如前后动词的时态以及那些貌似与题目无关的信息。
	二、完形填空
	命题趋势：近年来完形填空突出语篇；试题型稳定，选项独具匠心；辨析词义，以实词为主；记叙文为主，夹叙夹议，且第一局不设空；生词量有增无减；长句增加，句式灵活，结构复杂
	灵活的解题技巧：照应 ，瞻前顾后联系全文；积累，掌握固定搭配的用法与同义词的辨析；分清逻辑关系，注意文中分类、对比、从属、并列等关系所用的词语；化简句子，突破长句的理解；充分利用你的常识
	具体做法：第一遍粗做，大概看一遍文章，可确定很少一部分的选项；第二遍仔细阅读，几乎所有的选项都能做出来，第三遍再仔细阅读，认真检查选项，把个别遗留问题解决。
	三、阅读理解
	命题趋势：对语篇领悟能力的检测仍是高考的重点和基本点，选材更趋多样化、现代化和生活化，突显语言与文化相结合的特点 ，适当降低读速及难度要求，高试题的信息含量，体现新课程的理念 ，阅读材料更趋于“原汁原味”，设问方式更加深入 
	阅读考试技巧：
	首先，不要改变自己的阅读习惯；有的考生先看题目后看文章，有的是先看文章再看题目，你平时怎么练，考试就怎么做。
	　  其次，英语阅读最关键的是集中注意力。如果在阅读时注意力集中不起来，那么上下文的理解，之间的因果联系，还有时间关系就把握不清楚，而且印象也不深刻，在做题时，往往又要倒回去再阅读，这样不仅会占用很多时间，而且获得的信息也不是很准确。
	具体做法：做阅读时最好一边读一边将认为重要的部分划下来，这样做题时容易找到依据，节省时间，正确率也高。而对于长的文章，可以先读题，带着问题找答案。特别要注意问题中一些特别的词，如not、except等，考生在考试紧张的情况下最易发生粗心的情况。
	四、短文改错
	首先做题要讲究规范，一定按要求改。做时先将全文通读一遍，弄清文章大意；判断对错不但要从本行、本句考虑，还要顾及全文；如感觉一行有两处错，应将较有把握处改正；每行无论多词、缺词或错词，做题时只能动一个词。高考改错题允许不设正确行，若有只有一行；错误设在拐弯处和起首处最容易被忽略。
	五、书面表达 
	命题趋势：命题式作文，半开放式，校园词汇要抓紧，环保题材要关注，低碳生活(a low-carbon lifestyle)是热点
	写作要注意留够时间，至少需要20分钟。仔细审题至关重要，仔细研究所提供的文字和图画(图表)材料和作文要求，分析、提炼要点，理顺要点，确立基本的写作思路，不要忽略任何一个词，关键词更不能遗漏，构思好写几个方面，缺一不可；一般分为三段，有头有尾，比较完整；　　
	在正式写作前一定打好草稿，正式写作时应注意卷面整洁，不可东涂西改，因此失分。
	覆盖所有的语言要点。 内容要点可用不同  方式表达。对紧扣主题的适当发挥不予扣分。
	应用词汇和语法结构的数量和准确性。值得注意的是 语法结构或词汇方面虽有些许错误,  但为尽力使用较复杂结构或较高级词汇所致;
	具有较强的语言运用能力，避免汉式英语
