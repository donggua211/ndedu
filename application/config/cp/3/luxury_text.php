	<b>方案：</b>
	课外阅读是相对于课文阅读教学而言的,是指学生在课外独立的阅读活动,是课堂阅读的继续和扩展,是阅读能力训练的重要方面。课外阅读是语文课所必须的和独有的,这是由语文课本身的性质决定的。 
	语言文字的积累,其途径是听和读;语言文字的表达,其形式是说和写。这就需要我们语文教师树立大语文观,去引导学生在会读一本书的基础上,走出课堂,走向生活,广泛开展课外阅读活动,博览群书,吸收名家名篇的语言精华。开展语文课外阅读,不仅有利于学生开拓视野,陶冶情操,而且也是学生积累语言,丰富知识的有效方法。
	课外阅读由于脱离了语文课堂教学的时间、空间的限制，更具有自主性和灵活性，同时也具有不确定性。初中生正处于生长发育期，心理尚不成熟，因而大部分未能形成自觉的阅读习惯。同时对课外阅读缺乏明确的目标，没有正确的阅读方法，这都需要教师作具体指导。
	激发阅读兴趣
	孔子曾说过，对于一件事，“知之者不如好之者，好之者不如乐之者。”如果学生对课外阅读产生了兴趣，何愁他们不去读呢？何愁他们不读得“欣然忘食”呢？因此，教师在这其中最重要的工作就是激发学生的阅读兴趣。那么，具体该怎样做呢？ 
	1、以读促读
	学生在课内读了一篇感兴趣的文章后，进而引导他们读与之相关的一组文章。此外，还可以由这位作家的某一篇文章延伸到他的其他文章，或由所选作品延伸到背景材料、评论性文章等。教材无非是个例子，只要教师善于引导学生举一反三，拓宽视野，在广阔的课外阅读中涵泳、品味，就能真正做到“得道于课内，得益于课外”。
	2、以说促读   
	教师定期组织阅读积极分子介绍文坛新动态，推介新作品，谈自己的阅读心得，讲讲课外阅读的收益。有了榜样示范，有积极分子带动，整个班级的阅读氛围自然要好许多。
	3、以看促读    
	在这样一个资讯社会，各种各样的诱惑和刺激正与书争夺着那些单纯的花季少年。在许多孩子眼里，电视、网络游戏远比书来得亲切生动。这也是为什么现代人读书量持续走低的重要原因。不过，凡事都有两面性，关键在于我们如何趋利避害。现在，有许多名著名篇都拍摄成了影视作品，如四大古典名著。这些影视作品大多已制成了光盘在市场上出售，假期中，各大电视台也纷纷播映。教师引导学生在读作品的同时，收看这些影视作品，他们对原著的阅读兴趣会大大提高。
	4、以诵促读    
	有位外国朋友曾经说过，你们中国人说话就像唱歌似的，真是美极啦！为什么汉语有这样神奇的音感效果呢？原因之一，汉语是有声调的语言。不同声调的语汇组合排列起来，就会形成抑扬顿挫的音乐美。     
	教师如能利用汉语的这一优势，指导学生朗诵课外读物中的精美语段，势必能激发学生课外阅读的兴趣。而且，朗诵的过程是学生对作品不断认识的过程，朗诵是一种艺术创造！要想朗诵好，就必须潜心钻研原作的风格和感情，这样必然会加深对原作的理解和喜爱。当学生爱上了朗诵，可以说他也爱上阅读了。5、以写促读     教师结合学生阅读的材料，布置写作的话题。
