	二、有一个小男孩练琴时每天坚持4个小时。她的老师知道后，对他说：“你不能这样练，马上停止。因为长大以后根本没有更多的时间来练琴，你应该养成习惯，一有空闲就练，即使几分钟也行。”他听从了老师的劝告，把练钢琴的时间分解到各个时间段。其它时间他用来写日记、培植标本、到草地上踢足球，而这一切，并没影响他的琴艺。这个美国小男孩后来成为著名的诗人、小说家和极其出色的钢琴家，他之所以在各个领域取得辉煌的成就，原因在于他能分解自己的爱好到每天的时间中，他即使只有5分钟的空闲也会利用起来，写几句诗，弹一首曲子。几分钟的时间并不长，但如果能利用它并能成为一种习惯，这些短短的时间就有可能成就一个人，因为再大的事业和成就所需要的数年和数十年的时间都是由短短的几分钟累加起来的。当然这些应该是毫不拖延并加以充分利用的几分钟。 
	通过这两个故事，你是否明白，怎样提高时间的性价比，没错，每个人的时间都是一样的，怎样在有限的时间获得更多的东西，除了坚持的毅力外，还需要方法。

	<b>方案：</b>
	学习方法是通过学习实践，总结出的快速掌握知识的方法。因其以学习掌握知识的效率有关，越来越受到人们的重视。学习方法，并没有统一的规定，因个人条件不同，选取的方法也不同。其中，有人专门总结的特殊定向的学习训练方法，如：速记，等，可对其他学习者，产生启发效果和借鉴作用。
	小学阶段最主要的就是语文，数学和英语的学习，那么在这里，我们将会给出很具体的针对每一个科学习的学习方法，让孩子轻松快乐的学习。
	<b>语文：</b>
	微观具体的做法叫做学习方法，宏观整体的做法叫学习策略。语文学习有具体的方法，也有整体的策略。“最有价值的知识是关于方法的知识。”在未来社会里，“文盲不再是不识字的人，而是没有学会怎样学习的人。”因此，对学生进行有关学习方法的指导是十分必要的。
	小学生语文的学习策略。所谓学习策略，主要是指在学习活动中，为达到一定的学习目标而学会学习的规则、方法和技巧；它是一种在学习活动中思考问题的操作过程；这是认识策略在学生学习中的一种表现形式。
	如何理解学生的学习策略呢？
	首先，重视学生的学习策略，就是承认学生在学习过程中的主体性，强调学生在学习活动中的积极作用和能动作用。第二，学生的学习策略是学会学习的前提，学会学习包含着学生运用一系列的学习策略；学生的学习策略是造成其学习个别差异的重要原因，对学生实行"因材施教"要考虑到他们学习策略的差异。第三，学习策略是一系列有目的的活动，它是学生在学习过程中所选择、使用、调节和控制学习方法、方式、技能、技巧的操作活动。最后，学生的学习策略实施的过程，也是实行决策的过程。
	学生的学习活动，主要是一种理性的认识或认知活动，一种思维或信息加工的的过程，这也是学习活动有别于人类一般认识活动的特征。
	知识有简单和复杂之分，简单知识学习在小学语文教学中，主要是词汇学习，其难点不在于理解而在保持，因此，如何依据记忆规律，促进知识的保持成为促进简单知识教学的主要问题。复杂知识学习，在小学语文教学中，主要指概念、规则的学习和阅读、写作的学习，其关键是理解，而理解的实质是学生知道新知识内部各要素之间及新旧知识之间的联系。以下是几种常见的学习策略。 
	（一）注意策略。语文教师可以采用外部手段来控制学生注意，并激发学生采用适当的注意策略，以促进学习与保持。我在让学生读课文时，就注意到"带着问题读课文"并不是一种最佳的阅读方法，而"让学生先读课文，然后思考问题"，反而是一种更有价值的学习策略。这是因为小学生的注意力比较差，一心几用的能力相对较弱。
	（二）编码与组织策略。人们注意了的信息要想能够长久地保持，最有效的策略就是对信息进行编码和组织。无论是拼音、识字、阅读教学，语文老师都需要经常引导学生对知识进行分类排队，找出规律，就是这种策略的运用。比如，学习“咨”这个字时，我让学生找出它的形近字“姿、资”来，进行对比，比较记忆，学生大大加深了对这几个字区别
	（三）精细加工策略。我常常教育学生，在阅读和听讲时，一边听（或一边看），一边要做笔记。有时可以画出文中自己觉得写得美或者写得好的句子，有时可以在句子旁边写下自己的简单感受。
	（四）复述与复习策略。如，学生为了记住汉字，必须一遍遍地读写生字。教师在语文教学中，应注意教育学生在阅读过程中，边阅读边背诵，将阅读和尝试背诵交替进行。这样的方法，有助于学生的记忆顺利进行。
	另外，对于复杂知识的学习，复习策略包括边看书边讲述材料、在阅读时对材料的重点、难点和要点用画线、圈点、加注符号等方式将其突现出来等。
	这些都是已被无数事实证明是行之有效的学习策略，我们在日常的语文教学活动中应有意识地对学生加以训练。

	小学生语文的学习能力
	语文教学的根本目的在于通过语文教学发展学生正确理解和使用祖国语言文字的能力，也是发展学生的语文能力。所谓能力是人们成功地完成某种活动所必需的个性心理特征，可分为一般能力和特殊能力两类。语文能力是一种特殊能力。它是以语文概括为基础的，由四种语文能力与五种思维品质组成的，有20个交结点的、开放性的动态系统。
	概括是语文能力的基础。对于小学生语文的听、说、读、写诸方面而言，概括有以下表现：听的关键是"听得好"。即会听，听得准确，抓住别人讲话的中心，理解所听内容的实质。这是在"听"中的概括能力的表现；说的关键是"说得清"。即逐步地掌握准确、鲜明、生动的口语表达特点，能做到用词准确、词达意明、语言通顺、层次分明、修辞恰当、逻辑性强。这是在"说"中的概括能力表现。
	读的要素较多，一是朗读与默读的阅读形式，二是理解词、句、段、篇和修辞结构的阅读内容，三是分析段落层次、获得中心思想、掌握文章脉络的逻辑思维能力。这三个方面都以学生的概括能力为基础。写作能力发展也是一个概括化的过程。小学生从“说”到“写”，从“读”到“写”（仿写）两个过渡，都要通过书面语言条理化地、生动地表达出事物的内在联系。这里就有一个综合提炼的过程，即概括过程。
	由于语文能力的基础是概括，所以，我们在培养小学生语文能力时，要自始至终地突出对其概括能力的训练。传统将学生的语文能力归纳为听、说、读、写四种基本能力，这种提法是正确的。如果说听、说、读、写反映了小学生语文能力的基本内容，那么，不同学生在语文学习中的差异则与他们在语文学习中思维品质的差异密切相关。
	思维品质是智力活动中，特别是思维活动中智力与能力特点在个体身上的表现。它体现了每个个体思维的水平和智力、能力的差异，通常包括深刻性、灵活性、独创性、批判性和敏捷性等五个方面。这五种思维品质，是一个彼此联系、不可分割的辩证统一体。
	我们要从听、说、读、写这四个方面出发，培养学生思维的深刻性、灵活性、独创性、批判性和敏捷性，这样，对于学生的语文学习能力，是非常重要的。

	语文的学习方法，有涉及语文整体的，有涉及语文局部的；有基本的，也有具体的。关于语文整体的基本学习方法，中外教育家、学者、作家都有这样或那样的论述。但多阅读、多观察、多练习和多思考看起来似乎是老生常谈，但却是学好语文的基本功。
	小学生学习语文的具体方法，可以分为字词的学习方法，句子的学习方法，段的学习方法，篇的学习方法，以及学习写作文的方法等。如：字词的学习方法有：运用汉字的构造规律来理解字的方法，组词法，造句法，查字典法，用词素分析理解词语的方法，同义法的比较法，根据语言环境理解词义的方法，追根溯源理解词义的方法，等等。 
	句子的学习方法有：扩写、缩写句子的方法，变换句式的方法，用关联词语理解简单复句的方法，修改病句的方法，等等。段的学习方法有：找中心句的方法，给段分层的方法，调整段中语序的方法，等等。 
	篇的学习方法有：分段的方法，归纳段意的方法，概括文章主要内容的方法，概括文章中心思想的方法，等等。 学习写作文的方法有：审题的方法，立意的方法，选材的方法，组织材料的方法，修改文章的方法，等等。
	一、怎样预习课文
	有不少小朋友认为只要上课时专心听讲，勤记笔记，课后认真完成作业，再加上自觉复习，就能使成绩提高。其实，这还不够。叶圣陶老爷爷说过：练习阅读的最重要阶段是预习。也就是说在老师上课之前，你先得自己学习一下课文，在预习中要尽量运用你已经获得的知识和方法去主动地解决自己能解决的问题，把不懂的问题记下来，在上课时跟老师、同学一起学习讨论。这样不仅学习效果好，而且培养了自己的自学能力。有了这种能力，即使以后没有老师教你，你自己也人看书学习了。为了帮助小朋友掌握预习的方法，我把预习的方法和过程编成一个顺口溜，帮助你记忆和运用。
	遇到生字查字典，读准字音，分清字形，组成词语划条线。联系上下文，据词来定义，然后动手写几遍，字形字义牢牢记。先将课文读一遍，主要讲什么，心中有个底。再将课文细细读，每节讲什么，概括成一句，内容相同合一段，掌握规律性，分段要合理。认真读课文，仔细想问题，课后提示作参考，学会自己提问题。读、划、批、注好办法，不动笔墨不看书。
　　二、怎样辨析同义词
　　同义词包括近义词。辨析同义词可从以下几方面去考虑：
　　⒈从感情色彩上进行辨析。也就是从词的褒贬义去辨析。如“顽强”与“顽固”前者表示赞许，后者则表示憎恨。
　　⒉从语意轻重上进行辨析。如“破坏”与“摧毁”前者程度浅，后者程度深。
　　⒊从范围大小上进行辨析。如“战争”与“战斗”前者范围大，后者范围小。
　　⒋从“具体”与“概括”的不同进行辨析。如“衣服”与“衬衫”，“衣服”是概括性的，指还有衣服，而“衬衫”是具体的，可以指长袖衬衫或短袖衬衫，男式衬衫或女式衬衫等。
　　⒌从指对象的不同去辨别。如“沉重”与“繁重”，都有分量重的意思，但“沉重”指精神或思想负担重，而“繁重”则指工作、任务的数量重。
　　⒍还可以从普通话和方言，如“讨饭”与“乞丐”，口头语与书面语，如“爸爸”与“父亲”，旧词与新词，如“薪水”与“工资”，外来语与本民族词，如“水门汀”和“水泥”常用语和专门术语，如“盐”和“氯化钠”等几方面进行辨析。
　　三、怎样找反义词
　　意义相反的词叫反义词，寻找反义词是否可从以下几方面着手：
　　⒈从词的音节去寻找。反义词的音节一定要和对应的词相等。如“强”是单音节的，它的反义词也必须是单音节的“弱”。
　　⒉从词的范围去找。反义词所指的范围应该是相同的，如“强壮”是指人的体质，它的反义词应找“瘦弱”，而不能找“软弱”。
　　⒊从词的性质去找。词按性质又分为名词、动词、形容词等。一对反义词的词性必须是相同的。如“黑暗”是形容词，它的反义词也应是形容词“光明”，如果用“亮光”就不对了。因为“亮光”是名词。
　　⒋从不同的语言环境中去寻找。有的词是反义词，词义不同，它的反义词也不同。如：
　　这鱼很新鲜。这句中的“新鲜”的反义词应是“腐烂”。
　　这是一件新鲜事。这句中的“新鲜”的反义词应是“陈旧”。
　　早晨的空气很“新鲜”。这句中的“新鲜”的反义词应是“混浊”
　　四、怎样理解句子的含义
　　理解句子有以下几种方法：
　　⒈要抓住句子中的关键性词语。如《十里长街送总理》一课中有这样一句话：一位满头银发的老奶奶，双手拄着拐杖，背靠着洋槐树，焦急而又耐心地等待着。我们可以抓住“焦急”和“耐心”这两个关键词。这两个词乍起来似乎矛盾，但它们却反映了老奶奶急切想见到总理的灵车，以及不见继车决不离去的那种对总理无比热爱的感情。
　　⒉联系上下文来理解。有些句子的含义与上下文有着密切的关系，只有顾及全篇，才能理解句子的含义。在古诗学习中尤其如此，如“死去元知万事空”，要理解它的真正含义还得联系下文：本来就知道人死了以后什么都没有了，下文是：“但悲不见九州同”但感到悲伤的是没有看到祖国的统一。最后两句是“王师北定中原日，家祭无忘告乃翁”。最念念不忘的是盼望南宋的军队能早日收复中原统一祖国，到地时家祭的时候不要忘记告诉你的父亲。反映了作者临死时念念不忘统一祖国的激情。
　　⒊联系实际，典型联想来理解。如《我的战友邱少云》一文中有这样一句话：这位伟大的战士，直到生命的最后一刻，也没挪动过一寸地方，没有发出一声呻吟。我们可以联想自己被沸水或火星烫着时会迅速地把手缩回来以及剧烈疼痛的情景，而邱少云在烈火烧达半小时之久却能做到纹丝不动。足见他的高度纪律性及为革命勇于献身的伟大精神。
　　⒋从比喻、象征意义中去理解。如《我的伯父鲁迅先生》一文中有这样一句话：“四周围黑洞洞的，还不容易碰壁吗。”作者用“黑洞洞”形容旧社会非常黑暗，用“碰壁”比喻革命者没有自由，到处受迫害。这是鲁迅先生用诙谐的笑谈来讽刺，抨击旧社会的黑暗。
　　⒌从联系时代背景来理解。有的文章要与当时地的情况联系起来才能理解。如“赶集”一文中有这样一句话：“料想四年以前该不会有这些东西吧”四年以前是什么时候呢？文章写的是解放初期农村面貌的迅速改变，农民的生活的不断改善，从而歌颂了领导好。
　　五、常见的病句有哪几种
　　（一）修改病句要记住：遵原意，少改动。它的具体步骤和方法是：
　　⒈读。读懂原句，揣摩说话人本来想说的是什么意思。然后找准病因，辨清病句的类别，是用词不当、成份残缺、搭配不当、重复累赘、词序颠倒、自相矛盾，还是分类不当。
　　⒉划。用铅笔在病句上划出需要修改有部位，以便针对病因，进行分析、修改。
　　⒊改。运用修改符号，进行删、补、换、移。即删去多余及错误的词语、使句子简明。补上句子残缺成份，使句子完整。替换有关词语，使用词怡当。移前挪后，调整词语位置，使语序正确。
　　⒋对。把修改后的句子进行复查性质的校对阅读。看看是否通顺，有无新的语病产生，是否把说话人原先想说的意思表达清楚了。如果发现有问题，还得重改。
　　（二）怎样修改病句
　　⒈词义混淆。因对词义概念理解模糊而选用词不当的语病。如：爱迪生发现了电灯。把“发现”和“发明”两词混淆。
　　⒉词类误用。因分不清词类而造成用词不当的语病。如：今天气候晴朗。把“气候”与“天气”混淆了。
　　⒊成份残缺。句子缺少不应该省略的主要成份。（主、谓、宾），造成句子结构不完整的语病。如在老师的教育下，进步得很快。（缺少主谓）雄伟美丽的天安门城楼。（少谓语）老师向全班同学提出下课十分钟不做作业。（少宾语）
　　⒋搭配不当。因句子中相关的成份之间互相不能搭配而造成的病句。如：我的家乡是宁波人。缩句变成“家乡是人”（主谓搭配不当）我们在同坡上种满了果园。（动宾搭配不当）我们把教室打扫得整整齐齐。（连带成份与中心词之间搭配不当）
　　⒌重复累赘。过多地动用修饰词语反而显得多余，豪迈了病句。如：在班队会上，张军首先第一个发言。“首先”与“第一”重复了。
　　⒍词序颠倒。因词语排列次序颠倒而造成病句如：老师的办公桌上放着一叠厚厚的考卷。考卷没有厚薄之分，显然是把一叠与厚厚的词序颠倒了。
　　六、怎样理解词义
　　常用的理解词语的方法主要有以下几种：
　　1、据词定义法。有些词有好几种意思，同一个词在不同的句子中的意思统统也不同，我们可以通过查字典，比较用哪个意思最合适就选哪一条解释。所以，勤查字典是理解词义的最好方法。
　　2、分解组合法。先逐个分析词语中每个词素的字义，然后再合起来理解。如“健美”，“健”是“健康”，“美”是“优美”，合起来就是“健康而优美”。
　　3、以旧带新法。有些新词是以几个旧字组合而成的，那么我们可以通过对旧字的理解带出新词的含义。如“无价之宝”，“无价”，没有价钱，“之”是“的”，“宝”是“宝贝”，合起来的意思是“用多少钱也买不到的宝贝”，指极为稀有的珍贵东西。
　　4、词义引申法。有些词有本来的意思，还可以引申其他方面的含义，对这类词的理解，我们要先弄清它的原来意思，再理解它的引申义。如“碰壁”原指把头撞在墙上，而在《我的伯父鲁迅先生》一文中有这样一句话：“碰墙”指的是在黑暗的旧社会，革命者处处受迫害的意思，是引申义。
　　希望小朋友能在学习过程中多加运用、培养自己理解词语的能力，千万不能死记硬背词语手册中的解释。
