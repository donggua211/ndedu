	孩子逐渐长大，更加会认识到钱的“好处”———能够买很多零食和玩具，这时候一定要继续培养孩子对储蓄的兴趣。可以带着孩子一起到银行，以孩子的名字开立账户，将压岁钱和平时积攒的零花钱存进去。
	这阶段，应该和孩子一起设计一下储蓄结构，比如，每年春节过后，将收到的压岁钱存成一年定期，第二年取出利息，再将本金和新一年的压岁钱转存定期，这样，真金白银的利息收入可以让孩子充分感觉到储蓄的妙处。而平时积攒的零花钱则以活期为主，达到一定数额之后再转存成期限短一些的定期存款，比如三个月。
	活期、短期存款与长期存款相结合，平时孩子有额外的消费时，从活期账户支配，而短期账户作为兼顾灵活与高收益与长期存款相配合，这样一来，孩子可以初步懂得搭配储蓄的合理性。
	2、零花钱的自由
	孩子逐渐长大，渴望有一定的自主权，在小学阶段，零花钱还应该是“定量”供给，但家长应该相应地将周期拉长，比如在三年级之前可以一个星期为一个周期，四到六年级以两个星期为一周期。这种情况下，孩子会感到有一定的自主支配权，“自由”的感觉会让孩子对理财产生一定的兴趣，对培养孩子独立自主的行为方式有好处。
	虽然给孩子一定自由，但零花钱的供给也要适量，避免孩子过于铺张浪费；同时，还是应该坚持“家庭小银行”的做法，以“利”诱惑孩子，提倡孩子节俭。
	3、支配“巨款”的信任
	孩子有了一定积蓄后，对于一些必需的“大件”用品，比如衣物、运动器具等，可以让孩子动用自己的积蓄去购买，这可以让孩子体会到成就感，而且能让孩子懂得，储蓄对于满足需求的作用，进一步对理财产生兴趣。
	4、让孩子学会花钱
	有了自主支配的零用钱，孩子有可能任意乱花，家长总是一味责怪孩子：怎么用了这么多钱？不如告诉孩子：怎么这么没有计划地花钱？效果大不一样。
	5、记录消费
	刚开始时，父母可以帮助孩子在领到零用钱时，就先把未来一个周期所需要的花费记录下来，额外的支出也要随后一一记录，养成孩子记账的习惯。几个月后，家长不但可依这份资金流量表，看看孩子的消费倾向，了解他对金钱的价值与感受，万一发现偏差，也可以适时纠正，或是作为奖励孩子节俭的依据。
	实例再现：
	小学老师杜乐月帮13岁的儿子制作了记账本，要求他即使买一根雪糕也要“记录在案”。孩子认真记录一个月后，很是吃惊：“没买啥东西，怎么就花了300多元？”仔细对账发现，笔记本掉了，花10元补买一本，请同学吃点心10元，买2张CD又是10元……原来积少成多，花费这么多。从此，孩子知道了节约。
　　特别提醒：记账不仅可以帮助孩子培养良好的理财意识和习惯，也很容易让他们理解花钱容易挣钱难。
	6、制订消费计划
	周期性的零花钱，对孩子来说，数目不小，为防止其毫无节制地乱花，家长应该帮孩子先拟定这一星期的消费计划，从必须到可有可无，按内容的重要依次排列，消费时，也按照这一次序，养成孩子有目的消费的习惯。
	7、学会做预算
	有了计划表，那么家长就可以帮助孩子学习做预算。根据消费计划中列出的内容，可以预先算出大致的花费，如果少于零花钱的数目，则鼓励孩子储蓄。超出零用钱的数目，就砍掉那些可有可无的项目，如果确实都是些必需用品，但却超出孩子的支配范围，那么孩子就可以动用储蓄或是从下一阶段的费用中预支。
