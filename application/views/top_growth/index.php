          <span class="font_orange"><br />
          精英成长计划目标：</span><br />
      确保每一位会员用最小的求学成本(时间、精力、资金等）获得阶段性学业目标所必需的成绩和能力，保障提高成绩，成功完成学业，提高综合素质，实现成功人生。<br />
      <strong><br />
        具体体现：</strong><br />
        1、3～6岁幼儿最好的教师是父母，尼德教育依据不同年龄不同教育需求，设计了具有实际操作性的幼儿教育课程，促进幼儿身体、智力、性格和情感的全面发展，为未来小学学业生活做好准备。<br />
        2、综合考虑幼儿个性特点、家庭成员背景情况，帮助家庭为幼儿设计不同主题情景学习区（星空、梦幻、数字、彩虹等），辅助幼儿成长。<br />
        3、针对不同学前幼儿综合发展不同，结合小学学习生活特点，辅助每一位学龄儿童学员顺利度过幼小衔接关键期，快速适应小学学习成长生活。<br />
        4、帮助学员从小成长良好的学习习惯（注意力、马虎）、生活习惯（自理能力、不受约束）、行为习惯（磨蹭、言行举止），确保学员学习成绩优异、学校生活快乐，升学顺畅。<br />
        5、从不同儿童不同成长环境、不同个性思维特点、不同成长需求辅助小学低年级（1～3）学员到小学高年级（4～6）学员平稳过渡，该衔接期的平稳过渡相对整个黄金学习期学习至关重要。<br />
        6、针对不同学员目前学习方法（偏科、厌学、缺乏上进心）、个性发展（任性、缺乏责任感、孤僻）、人际交往（老师冲突、同伴关系）情况，帮助每一位学员总结并掌握适合自己的学习方法、健全个性发展、提高人际交往能力。<br />
        7、综合考虑学员学习、兴趣爱好、知识结构、家庭环境，帮助学员建立个性化家庭图书屋，精选优质适合学员年龄、心理特点的图书，强化学员兴趣爱好方向，补全知识结构盲区，建立其在某一方面的的发展优势，逐渐延伸到其他方面，最终实现整体发展。<br />
        8、引导适龄学员了解认识青春期生理、心理特点，安然度过青春期，不早恋、不迷恋网络。<br />
